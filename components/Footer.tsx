async function getSocials(): Promise<SocialLink[]> {
  const query = `*[_type == "social"] | order(order asc) {
    _id,
    name,
    url,
    platform
  }`;
  try {
    return await client.fetch<SocialLink[]>(query, {}, { next: { revalidate: 60 } });
  } catch (error) {
    console.error("Failed to fetch socials:", error);
    return [];
  }
}

